SELECT
	RI.RETURN_ID ,
	RI.ORDER_ID ,
	RI.RETURN_QUANTITY ,
	RI.RETURN_REASON_ID ,
	RH.RETURN_DATE
FROM
	RETURN_ITEM RI
LEFT JOIN RETURN_HEADER RH
ON
	RH.RETURN_ID = RI.RETURN_ID
WHERE
	EXISTS
(
	SELECT
		1
	FROM
		RETURN_ITEM RI2
	WHERE
		RI.RETURN_ID = RI2.RETURN_ID
	GROUP BY
		RI2.ORDER_ID
	HAVING
		COUNT(RI2.RETURN_ITEM_SEQ_ID) > 1)

